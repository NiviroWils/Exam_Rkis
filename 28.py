# Инициализация суммы
total_sum = 0

while True:
    # Ввод числа с клавиатуры
    number = float(input("Введите число (99999 для завершения): "))

    # Проверка на ввод числа 99999 для завершения
    if number == 99999:
        break

    # Проверка на ввод нуля и вывод текущего значения суммы
    elif number == 0:
        print("Текущая сумма:", total_sum)

    # Если введено ненулевое число, добавляем его к сумме
    else:
        total_sum += number

# Вывод итоговой суммы после завершения цикла
print("Итоговая сумма:", total_sum)
